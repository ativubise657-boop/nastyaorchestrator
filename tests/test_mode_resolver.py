"""Тесты для worker/mode_resolver.py — определение режима выполнения задачи.

Чистая логика без внешних зависимостей — моки не нужны.
"""
import pytest

from worker.mode_resolver import resolve_mode


# ---------------------------------------------------------------------------
# Тест 1: ключевые слова ревью → режим "rev"
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("prompt", [
    "сделай ревью этого кода",
    "review my code please",
    "rev этот файл",
    "проверь код — есть баги",
    "code review нужен срочно",
    "посмотри код, там что-то не так",
    "что не так с кодом?",
])
def test_resolve_mode_rev_keywords(prompt):
    """Промпты с маркерами ревью → режим 'rev'."""
    assert resolve_mode(prompt) == "rev"


# ---------------------------------------------------------------------------
# Тест 2: ключевые слова ag+ → режим "ag+"
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("prompt", [
    "ag+ нужна команда агентов",
    "рефакторинг всего модуля",
    "refactor этот файл",
    "несколько файлов надо обновить",
    "большой рефактор",
    "масштабный проект",
    "архитектура нового модуля",
    "мигрировать на новый стек",
    "migrat database to postgres",
])
def test_resolve_mode_ag_plus_keywords(prompt):
    """Промпты с маркерами ag+ → режим 'ag+'."""
    assert resolve_mode(prompt) == "ag+"


# ---------------------------------------------------------------------------
# Тест 3: обычный промпт → режим "solo" (дефолт)
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("prompt", [
    "напиши функцию для сортировки",
    "объясни как работает asyncio",
    "исправь опечатку в строке 42",
    "добавь комментарий к классу",
    "переименуй переменную x в count",
])
def test_resolve_mode_solo_default(prompt):
    """Обычные промпты без маркеров → дефолтный режим 'solo'."""
    assert resolve_mode(prompt) == "solo"


# ---------------------------------------------------------------------------
# Тест 4: пустой промпт → "solo" (не падаем)
# ---------------------------------------------------------------------------

def test_resolve_mode_empty_prompt():
    """Пустой промпт → 'solo', не исключение."""
    assert resolve_mode("") == "solo"


# ---------------------------------------------------------------------------
# Тест 5: регистр не имеет значения
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("prompt, expected_mode", [
    ("REVIEW этот код", "rev"),
    ("РЕВЬЮ пожалуйста", "rev"),
    ("AG+ команда", "ag+"),
    ("РЕФАКТОРИНГ модуля", "ag+"),
    ("АРХИТЕКТУРА системы", "ag+"),
])
def test_resolve_mode_case_insensitive(prompt, expected_mode):
    """Определение режима нечувствительно к регистру."""
    assert resolve_mode(prompt) == expected_mode


# ---------------------------------------------------------------------------
# Тест 6: приоритет rev > ag+ (если есть оба маркера)
# ---------------------------------------------------------------------------

def test_resolve_mode_rev_takes_priority_over_ag_plus():
    """Если в промпте есть маркеры обоих режимов — rev проверяется первым."""
    # rev-маркер стоит первым в коде → должен победить
    prompt = "ревью и рефакторинг этого большого модуля"
    mode = resolve_mode(prompt)
    # ревью + рефакторинг — rev идёт первым в if-цепочке
    assert mode == "rev"


# ---------------------------------------------------------------------------
# Тест 7: промпт состоит только из пробелов → "solo"
# ---------------------------------------------------------------------------

def test_resolve_mode_whitespace_only():
    """Промпт из пробелов → 'solo'."""
    assert resolve_mode("   \t\n  ") == "solo"


# ---------------------------------------------------------------------------
# Тест 8: репрезентативные реальные промпты Насти
# ---------------------------------------------------------------------------

@pytest.mark.parametrize("prompt, expected_mode", [
    ("сделай кнопку синей", "solo"),
    ("добавь валидацию формы", "solo"),
    ("ревью API эндпоинтов", "rev"),
    ("нужна команда для рефакторинга каталога товаров", "ag+"),
    ("мигрировать БД на новую схему", "ag+"),
    ("почему тест падает?", "solo"),
    ("проверь код на баги", "rev"),
])
def test_resolve_mode_real_prompts(prompt, expected_mode):
    """Реальные промпты от пользователя → корректный режим."""
    assert resolve_mode(prompt) == expected_mode
